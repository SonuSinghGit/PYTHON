'''                 LOGICAL OPERATOR
     there are 3 type of operator in python
  1) or
 2) and 
 3) not   ''' 
print(2>3 or 3>2)   #true (at least one statement is true)

print(4>3 and 6<9) #true (both the given statement will true then it's work)
 
 #      NOT LOGICAL OPERATOR
  #it reverse the resultant boolean expression 
 #true become false and false become true'''

print(not 2>6 ) #true, but it's actually false
print(not 6>4) #false, but it's actually true 